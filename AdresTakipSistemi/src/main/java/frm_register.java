    private void btn_register_page2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_register_page2ActionPerformed
        // TODO add your handling code here:

        String Name = txt_name.getText();
        String Surname = txt_surname.getText();
        String userNAme = txt_userName.getText();
        String Passw = jp_password.getText();
       

        if (userNAme == "" || Passw == "" || Name == "") {

            JOptionPane.showMessageDialog(rootPane, " Field can not be empty !", "Login", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Admin p1 = new Person(userNAme, Passw, Name, Surname);
        
        boolean ok2 = DataManage.AddUser(p1);
        if (ok2) {

            JOptionPane.showMessageDialog(rootPane, " User registered !", "Login", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(rootPane, " username already exits !", "Login", JOptionPane.ERROR_MESSAGE);
        }
        
        frm_login login=new frm_login();
        login.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btn_register_page2ActionPerformed

